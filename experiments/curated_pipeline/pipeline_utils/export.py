"""Export functions: YOLO labels + retrieval-ready SKU folders."""

from __future__ import annotations

import logging
import shutil
from pathlib import Path

import numpy as np
from PIL import Image

from experiments.curated_pipeline import config

logger = logging.getLogger(__name__)

# ── Helpers ─────────────────────────────────────────────────────────────────


def _abs_to_yolo(box: list[int], img_w: int, img_h: int) -> tuple[float, ...]:
    """Convert absolute ``[x1, y1, x2, y2]`` to YOLO normalized ``(cx, cy, w, h)``."""
    x1, y1, x2, y2 = box
    cx = ((x1 + x2) / 2.0) / img_w
    cy = ((y1 + y2) / 2.0) / img_h
    w = (x2 - x1) / img_w
    h = (y2 - y1) / img_h
    return (round(cx, 6), round(cy, 6), round(w, 6), round(h, 6))


# ── Class mapping ───────────────────────────────────────────────────────────


def build_class_mapping(registry: dict[str, dict]) -> dict[str, int]:
    """Build a SKU-ID → numeric class-id mapping, sorted by SKU ID."""
    sorted_ids = sorted(registry.keys())
    return {sku_id: idx for idx, sku_id in enumerate(sorted_ids)}


# ── YOLO export ────────────────────────────────────────────────────────────


def export_yolo(
    image_path: str | Path,
    detections: list[dict],
    class_mapping: dict[str, int],
    output_dir: str | Path | None = None,
) -> dict:
    """Generate YOLO-format label file and ``data.yaml`` for one image.

    Parameters
    ----------
    image_path:
        Path to the source shelf image (used for image size and output stem).
    detections:
        List of dicts with keys ``box``, ``sku_id`` — enriched detection
        results after human confirmation.
    class_mapping:
        ``{sku_id: numeric_class_id}`` — see :func:`build_class_mapping`.
    output_dir:
        Directory for exported files.  Defaults to ``config.YOLO_EXPORT_DIR``.

    Returns
    -------
    dict with keys ``label_file``, ``yaml_file``, ``n_labels``.
    """
    out_dir = Path(output_dir or config.YOLO_EXPORT_DIR)
    out_dir.mkdir(parents=True, exist_ok=True)

    src = Path(image_path)
    img = Image.open(src).convert("RGB")
    img_w, img_h = img.size
    stem = src.stem

    # ── Generate .txt ──────────────────────────────────────────────────────
    label_path = out_dir / f"{stem}.txt"
    lines: list[str] = []
    for det in detections:
        sku_id = det.get("sku_id")
        if sku_id is None or sku_id not in class_mapping:
            logger.warning("Skipping detection with unknown SKU %s", sku_id)
            continue
        cls_id = class_mapping[sku_id]
        cx, cy, w, h = _abs_to_yolo(det["box"], img_w, img_h)
        lines.append(f"{cls_id} {cx} {cy} {w} {h}")

    label_path.write_text("\n".join(lines) + "\n")
    logger.info("YOLO labels written to %s (%d objects)", label_path, len(lines))

    # ── Generate data.yaml ─────────────────────────────────────────────────
    class_names = [""] * len(class_mapping)
    for sku_id, cls_id in class_mapping.items():
        class_names[cls_id] = sku_id

    yaml_path = out_dir / "data.yaml"
    yaml_lines = [
        f"# Auto-generated by curated_pipeline export_yolo",
        f"# Source: {src.name}",
        f"",
        f"nc: {len(class_mapping)}",
        f"names: [{', '.join(class_names)}]",
        f"",
    ]
    yaml_path.write_text("\n".join(yaml_lines) + "\n")
    logger.info("data.yaml written to %s", yaml_path)

    return {
        "label_file": str(label_path.resolve()),
        "yaml_file": str(yaml_path.resolve()),
        "n_labels": len(lines),
    }


# ── Retrieval export ───────────────────────────────────────────────────────


def export_retrieval_folders(
    registry: dict[str, dict],
    output_dir: str | Path | None = None,
    copy_files: bool = True,
) -> dict:
    """Create ImageNet-style per-SKU folders for retrieval / few-shot experiments.

    Structure::

        {output_dir}/
            SKU-001/
                crop_000.jpg
                crop_001.jpg
            SKU-002/
                crop_000.jpg
            classes.txt

    Parameters
    ----------
    registry:
        Full SKU registry data from ``SKURegistry.get_all_entries()``.
        Each entry must have ``exemplars`` (list of file paths).
    output_dir:
        Root output directory.  Defaults to ``config.RETRIEVAL_EXPORT_DIR``.
    copy_files:
        If True, copy crop files.  If False, create symlinks (saves space).

    Returns
    -------
    dict with keys ``output_dir``, ``n_skus``, ``n_files``.
    """
    out_dir = Path(output_dir or config.RETRIEVAL_EXPORT_DIR)
    out_dir.mkdir(parents=True, exist_ok=True)

    total_files = 0
    sorted_skus = sorted(registry.keys())

    for sku_id in sorted_skus:
        entry = registry[sku_id]
        sku_dir = out_dir / sku_id
        sku_dir.mkdir(parents=True, exist_ok=True)

        for idx, src_path_str in enumerate(entry.get("exemplars", [])):
            src = Path(src_path_str)
            if not src.exists():
                logger.warning("Exemplar not found, skipping: %s", src)
                continue

            dst = sku_dir / f"crop_{idx:03d}{src.suffix}"
            if copy_files:
                shutil.copy2(str(src), str(dst))
            else:
                if dst.exists():
                    dst.unlink()
                dst.symlink_to(src.resolve())

            total_files += 1

        logger.debug("  %s → %d exemplars", sku_id, len(entry.get("exemplars", [])))

    # ── classes.txt ─────────────────────────────────────────────────────────
    classes_path = out_dir / "classes.txt"
    classes_path.write_text("\n".join(sorted_skus) + "\n")
    logger.info(
        "Retrieval export: %d SKUs, %d files → %s",
        len(sorted_skus),
        total_files,
        out_dir,
    )

    return {
        "output_dir": str(out_dir.resolve()),
        "n_skus": len(sorted_skus),
        "n_files": total_files,
    }
